# Анонимная функция для сложения двух чисел
sum_numbers = lambda a, b: a + b

# Запрос чисел у пользователя
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

# Используем анонимную функцию для вычисления суммы
result = sum_numbers(num1, num2)

# Выводим результат
print(f"Сумма чисел {num1} и {num2} равна {result}.")